'use strict';

/**
 * Represents a user.
 */
export class User {

    /**
     * Given/first name
     *
     * @type {string}
     * @memberof User
     */
    public FirstName: string;

    /**
     * Last name/surname
     *
     * @type {string}
     * @memberof User
     */
    public LastName: string;

    /**
     * Primary key/ID
     *
     * @type {number}
     * @memberof User
     */
    public UserID: number;
}
